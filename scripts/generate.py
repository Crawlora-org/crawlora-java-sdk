#!/usr/bin/env python3
"""Java SDK emitter.

Language-neutral spec parsing, grouping, aliasing, and the operations docs table
live in the vendored ``scripts/_sdkgen/core.py`` (synced from the API repo). This
file only maps OpenAPI types to Java types and writes the generated Java
artifacts:

  - ``src/main/java/net/crawlora/Operations.java`` -- runtime operation metadata
    (an immutable ``Map<String, Operation>``), the ``GROUPS`` map, and
    ``OPERATION_COUNT``.
  - ``src/main/java/net/crawlora/OperationId.java`` -- SCREAMING_SNAKE constants
    mapping to operation-id strings.
  - ``src/main/java/net/crawlora/groups/<Group>Group.java`` -- one generated
    group class per group, with a typed method per operation delegating to the
    client's dynamic ``request``.
  - ``docs/operations.md`` -- the shared operations table.

There are 330 operations, so we deliberately do NOT emit a distinct compiled
record per operation. Metadata is modeled with the hand-written plain classes in
``net.crawlora`` (Operation, QueryParam, FormParam); the generated file just
populates them.
"""
import json
import os
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from _sdkgen import core  # noqa: E402

ROOT = Path(__file__).resolve().parents[1]
DEFAULT_SPEC = ROOT / "openapi" / "public.json"
SPEC_PATH = Path(os.environ.get("CRAWLORA_OPENAPI_SPEC", DEFAULT_SPEC))

GENERATED_NOTE = "// Generated by scripts/generate.py. Do not edit manually."

# Java reserved words; a generated method name that collides gets a trailing "_".
JAVA_KEYWORDS = frozenset(
    """
    abstract assert boolean break byte case catch char class const continue
    default do double else enum extends final finally float for goto if
    implements import instanceof int interface long native new package private
    protected public return short static strictfp super switch synchronized this
    throw throws transient try void volatile while true false null var yield
    record
    """.split()
)


def _lower_camel(parts):
    if not parts:
        return "call"
    head, *tail = parts
    return head + "".join(p[:1].upper() + p[1:] for p in tail)


POLICY = core.NamingPolicy(
    case_fn=_lower_camel,
    dedup_sep="",
    keywords=JAVA_KEYWORDS,
    tag_group_overrides={
        "AppStore": "appStore",
        "CoinGecko": "coinGecko",
        "GooglePlay": "googlePlay",
        "ProductHunt": "productHunt",
        "SimilarWeb": "similarWeb",
        "SpotifyPodcasts": "spotifyPodcasts",
        "TikTok": "tiktok",
        "YouTube": "youtube",
    },
)


def java_string(value):
    """A Java string literal. ``json.dumps`` produces valid Java escaping for
    the characters that appear in the spec (quotes, backslashes, unicode)."""
    return json.dumps(value)


def java_type(param):
    """Map an OpenAPI parameter to a human-readable Java type for the docs
    table and Javadoc."""
    if (param.get("enum") or param.get("items", {}).get("enum")):
        return "String"
    typ = param.get("type")
    if typ == "integer":
        return "Integer"
    if typ == "number":
        return "Double"
    if typ == "boolean":
        return "Boolean"
    if typ == "array":
        return "List<" + java_type(param.get("items", {"type": "string"})) + ">"
    return "String"


def _string_list(values):
    return "List.of(" + ", ".join(java_string(v) for v in values) + ")"


def _query_param(param):
    args = [java_string(param["name"])]
    args.append("true" if param.get("required") else "false")
    args.append(java_string(param.get("type", "")))
    args.append(_string_list(core.enum_values(param)))
    return "new QueryParam(" + ", ".join(args) + ")"


def _form_param(param):
    args = [java_string(param["name"])]
    args.append("true" if param.get("required") else "false")
    args.append(java_string(param.get("type", "")))
    args.append(_string_list(core.enum_values(param)))
    return "new FormParam(" + ", ".join(args) + ")"


def _query_list(params):
    if not params:
        return "List.of()"
    return "List.of(\n            " + ",\n            ".join(_query_param(p) for p in params) + ")"


def _form_list(params):
    if not params:
        return "List.of()"
    return "List.of(" + ", ".join(_form_param(p) for p in params) + ")"


def _operation_literal(op):
    body_param = op["bodyParam"]
    body_param_arg = java_string(body_param) if body_param is not None else "null"
    cursor = op.get("cursorParams") or []
    parts = [
        java_string(op["id"]),
        java_string(op["method"]),
        java_string(op["path"]),
        _string_list(op["pathParams"]),
        _query_list(op["queryParams"]),
        _form_list(op["formParams"]),
        body_param_arg,
        "true" if op["bodyRequired"] else "false",
        _string_list(op["security"]),
        "true" if op.get("paginatable") else "false",
        _string_list(cursor),
    ]
    return "new Operation(\n            " + ",\n            ".join(parts) + ")"


def render_operations(model):
    lines = [
        "package net.crawlora;",
        "",
        GENERATED_NOTE,
        "",
        "import java.util.LinkedHashMap;",
        "import java.util.List;",
        "import java.util.Map;",
        "",
        "/**",
        " * Runtime operation metadata for every public Crawlora operation, generated",
        " * from the shared OpenAPI contract. {@link CrawloraClient} reads this at",
        " * runtime for validation and dispatch.",
        " */",
        "public final class Operations {",
        "    private Operations() {}",
        "",
        f"    /** Total number of operations in the contract. */",
        f"    public static final int OPERATION_COUNT = {model.operation_count};",
        "",
        "    /** Immutable map of operation id to its runtime metadata. */",
        "    public static final Map<String, Operation> OPERATIONS = buildOperations();",
        "",
        "    /** Immutable map of group name to {methodName: operationId}. */",
        "    public static final Map<String, Map<String, String>> GROUPS = buildGroups();",
        "",
        "    private static Map<String, Operation> buildOperations() {",
        "        Map<String, Operation> m = new LinkedHashMap<>();",
    ]
    # Chunk the puts into helper methods so no single method approaches the JVM's
    # 64KB per-method bytecode limit as the contract grows.
    chunk_size = 60
    op_items = list(model.operations.items())
    chunk_count = (len(op_items) + chunk_size - 1) // chunk_size
    for chunk_index in range(chunk_count):
        lines.append(f"        putOperations{chunk_index}(m);")
    lines.extend([
        "        return Map.copyOf(m);",
        "    }",
        "",
    ])
    for chunk_index in range(chunk_count):
        lines.append(f"    private static void putOperations{chunk_index}(Map<String, Operation> m) {{")
        for operation_id, op in op_items[chunk_index * chunk_size:(chunk_index + 1) * chunk_size]:
            lines.append(f"        m.put({java_string(operation_id)}, {_operation_literal(op)});")
        lines.append("    }")
        lines.append("")
    lines.extend([
        "",
        "    private static Map<String, Map<String, String>> buildGroups() {",
        "        Map<String, Map<String, String>> g = new LinkedHashMap<>();",
    ])
    for group_name, methods in model.groups.items():
        lines.append("        {")
        lines.append("            Map<String, String> group = new LinkedHashMap<>();")
        for method_name, operation_id in methods.items():
            lines.append(f"            group.put({java_string(method_name)}, {java_string(operation_id)});")
        lines.append(f"            g.put({java_string(group_name)}, Map.copyOf(group));")
        lines.append("        }")
    lines.extend([
        "        return Map.copyOf(g);",
        "    }",
        "}",
        "",
    ])
    return "\n".join(lines)


def render_operation_ids(model):
    lines = [
        "package net.crawlora;",
        "",
        GENERATED_NOTE,
        "",
        "/**",
        " * Constants for every operation id, named in SCREAMING_SNAKE_CASE. Use these",
        " * with {@link CrawloraClient#request} instead of hard-coding id strings.",
        " */",
        "public final class OperationId {",
        "    private OperationId() {}",
        "",
    ]
    used = set()
    for operation_id, meta in sorted(model.meta.items(), key=lambda item: item[1]["type_base"]):
        const = "_".join(core.words(meta["type_base"])).upper() or "OPERATION"
        base = const
        i = 2
        while const in used:
            const = f"{base}_{i}"
            i += 1
        used.add(const)
        lines.append(f"    public static final String {const} = {java_string(operation_id)};")
    lines.extend(["}", ""])
    return "\n".join(lines)


def _group_class_name(group_name):
    return core.type_name(group_name, "group")


def render_group(group_name, methods, model):
    klass = _group_class_name(group_name)
    lines = [
        "package net.crawlora.groups;",
        "",
        GENERATED_NOTE,
        "",
        "import java.util.Map;",
        "import net.crawlora.CrawloraClient;",
        "import net.crawlora.OperationGroup;",
        "import net.crawlora.RequestOptions;",
        "",
        "/**",
        f" * Generated typed accessor for the {java_string(group_name)} operation group.",
        f" * Reach it via {{@code client.{group_name}()}}.",
        " */",
        f"public final class {klass} {{",
        "    private final CrawloraClient client;",
        "",
        f"    public {klass}(CrawloraClient client) {{",
        "        this.client = client;",
        "    }",
        "",
    ]
    for method_name, operation_id in methods.items():
        meta = model.meta[operation_id]
        has_required = meta["has_required_params"]
        note = meta.get("note") or ""
        doc = [
            "    /**",
            f"     * {operation_id} ({meta['method']} {meta['path']}).",
        ]
        if note:
            doc.append(f"     * {note}")
        doc.extend([
            "     *",
            "     * @param params operation parameters",
            "     * @return the parsed response",
            "     */",
        ])
        lines.extend(doc)
        lines.append(f"    public Object {method_name}(Map<String, Object> params) {{")
        lines.append(f"        return {method_name}(params, null);")
        lines.append("    }")
        lines.append("")
        lines.append(f"    /** {operation_id} with explicit {{@link RequestOptions}}. */")
        lines.append(f"    public Object {method_name}(Map<String, Object> params, RequestOptions options) {{")
        lines.append(f"        OperationGroup.checkParams({java_string(operation_id)}, params);")
        lines.append(f"        return client.request({java_string(operation_id)}, params, options);")
        lines.append("    }")
        lines.append("")
        if not has_required:
            lines.append(f"    /** {operation_id} with no parameters. */")
            lines.append(f"    public Object {method_name}() {{")
            lines.append(f"        return {method_name}(Map.of(), null);")
            lines.append("    }")
            lines.append("")
    lines.append("}")
    lines.append("")
    return klass, "\n".join(lines)


def main():
    if not SPEC_PATH.exists():
        raise SystemExit(f"public OpenAPI spec not found: {SPEC_PATH}")
    spec = json.loads(SPEC_PATH.read_text())
    (ROOT / "openapi").mkdir(exist_ok=True)
    target_spec = ROOT / "openapi" / "public.json"
    if SPEC_PATH.resolve() != target_spec.resolve():
        target_spec.write_text(SPEC_PATH.read_text())

    model = core.build_model(spec, POLICY)

    base = ROOT / "src" / "main" / "java" / "net" / "crawlora"
    base.mkdir(parents=True, exist_ok=True)
    (base / "Operations.java").write_text(render_operations(model))
    (base / "OperationId.java").write_text(render_operation_ids(model))

    groups_dir = base / "groups"
    groups_dir.mkdir(exist_ok=True)
    # Remove stale generated group files so renames/removals stay deterministic.
    for existing in groups_dir.glob("*Group.java"):
        existing.unlink()
    for group_name, methods in model.groups.items():
        klass, source = render_group(group_name, methods, model)
        (groups_dir / f"{klass}.java").write_text(source)

    (ROOT / "docs").mkdir(exist_ok=True)
    (ROOT / "docs" / "operations.md").write_text(
        core.operation_docs(model, title="Crawlora Java SDK Operations", type_render=java_type)
    )

    print(f"generated {model.operation_count} operations across {len(model.groups)} groups")


if __name__ == "__main__":
    main()
